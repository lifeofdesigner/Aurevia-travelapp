function normalizeSearchText(value: string) {
  return value
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[^a-z0-9]+/g, " ")
    .trim()
    .replace(/\s+/g, " ");
}

export function getFlightAirportDisplayValue(airport: FlightAirportOption) {
  return `${airport.cityName} (${airport.iataCode})`;
}

export function getFlightAirportSearchText(airport: FlightAirportOption) {
  return normalizeSearchText(
    [
      airport.cityName,
      airport.airportName,
      airport.iataCode,
      airport.countryName,
      getFlightAirportDisplayValue(airport)
    ].join(" ")
  );
}

export function getFlightAirportByCode(code: string) {
  const normalizedCode = normalizeSearchText(code);

  return (
    FLIGHT_AIRPORT_OPTIONS.find(
      (airport) => normalizeSearchText(airport.iataCode) === normalizedCode
    ) ?? null
  );
}

export function resolveFlightAirport(value: string) {
  const normalizedValue = normalizeSearchText(value);

  if (!normalizedValue) {
    return null;
  }

  return (
    FLIGHT_AIRPORT_OPTIONS.find(
      (airport) => normalizeSearchText(airport.iataCode) === normalizedValue
    ) ??
    FLIGHT_AIRPORT_OPTIONS.find(
      (airport) => normalizeSearchText(airport.cityName) === normalizedValue
    ) ??
    FLIGHT_AIRPORT_OPTIONS.find(
      (airport) => normalizeSearchText(airport.airportName) === normalizedValue
    ) ??
    FLIGHT_AIRPORT_OPTIONS.find(
      (airport) =>
        normalizeSearchText(getFlightAirportDisplayValue(airport)) === normalizedValue
    ) ??
    null
  );
}

export function normalizeFlightAirportValue(value: string) {
  return resolveFlightAirport(value)?.iataCode ?? value.trim().toUpperCase();
}
